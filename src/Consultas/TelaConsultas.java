    private void PVActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_PVActionPerformed
        DefaultTableModel modelo = (DefaultTableModel) jTable2.getModel();
        modelo.setNumRows(0);
        String sql = "select  produto.nome, count (vendas_prod.Id_prod) from Vendas_prod INNER JOIN produto on Vendas_prod.Id_prod = produto.id_produto_venda " + 
        "where vendas_prod.data = '" + DtPesquisa.getText() + "' group by produto.nome;";
        Conecta_banco.ExecuteSQL(sql);
        try {
            while (Conecta_banco.resultset.next()) {
                modelo.addRow(new Object[]{Conecta_banco.resultset.getString("Nome"), Conecta_banco.resultset.getString("count")});
                }
        } catch (SQLException erro) {
            JOptionPane.showMessageDialog(null, "Erro ao listar" + erro);
        }
    }//GEN-LAST:event_PVActionPerformed

    private void PVDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_PVDActionPerformed
        DefaultTableModel modelo = (DefaultTableModel) jTable2.getModel();
        modelo.setNumRows(0);
        String sql = "select produto.nome, vendas_prod.data, vendas_prod.custo, vendas_prod.valor from Vendas_prod" +
        " INNER JOIN produto on Vendas_prod.Id_prod = produto.id_produto_venda order by produto.nome;";
        Conecta_banco.ExecuteSQL(sql);
        try {
            while (Conecta_banco.resultset.next()) {
                modelo.addRow(new Object[]{Conecta_banco.resultset.getString("Nome"), Conecta_banco.resultset.getString("Data"), Conecta_banco.resultset.getString("Custo"), Conecta_banco.resultset.getString("Valor")});
                }
        } catch (SQLException erro) {
            JOptionPane.showMessageDialog(null, "Erro ao listar" + erro);
        }
    }//GEN-LAST:event_PVDActionPerformed

